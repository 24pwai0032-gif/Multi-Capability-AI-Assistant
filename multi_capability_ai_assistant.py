from groq import Groq
import textwrap

class MultiCapabilityAssistant:
    """
    A Multi-Capability AI Assistant powered by Groq (llama-3.3-70b-versatile).

    Capabilities:
      1. Enhanced Email Writer  — research + draft emails in any tone
      2. Smart Summarizer       — summarize text with analytics
      3. Chat Assistant         — conversational chat with memory

    Tools: Calculator, Web Search, Data Analyzer, Weather
    """

    MODEL = "llama-3.3-70b-versatile"  # Free Groq model
    MAX_TOKENS = 1024

    def __init__(self, api_key: str):
        """Initialize the assistant with a Groq API key."""
        if not api_key:
            raise ValueError("❌ GROQ_API_KEY is missing! Add it to Colab Secrets.")

        self.client = Groq(api_key=api_key)
        self.chat_history = []  # Persistent memory for Chat capability
        self.tools = TOOL_SCHEMAS
        self.functions = TOOL_FUNCTIONS
        print("✅ MultiCapabilityAssistant initialized!")
        print(f"   Model: {self.MODEL}")

    # ─────────────────────────────────────────────
    # CORE: Tool Execution Engine
    # ─────────────────────────────────────────────
    def _execute_tool(self, tool_name: str, tool_args: dict) -> str:
        """Execute a tool by name and return its result as a string."""
        if tool_name not in self.functions:
            return json.dumps({"error": f"Unknown tool: {tool_name}"})
        try:
            return self.functions[tool_name](**tool_args)
        except Exception as e:
            return json.dumps({"error": f"Tool '{tool_name}' failed: {str(e)}"})

    def _call_llm(self, messages: list, use_tools: bool = True) -> any:
        """Call the Groq LLM with optional tool use. Returns the response object."""
        kwargs = {
            "model": self.MODEL,
            "messages": messages,
            "max_tokens": self.MAX_TOKENS,
            "temperature": 0.7,
        }
        if use_tools:
            kwargs["tools"] = self.tools
            kwargs["tool_choice"] = "auto"

        return self.client.chat.completions.create(**kwargs)

    def _run_agent_loop(self, messages: list) -> str:
        """
        Agentic loop: keeps calling tools until the model produces
        a final text response with no more tool calls.
        """
        while True:
            response = self._call_llm(messages)
            msg = response.choices[0].message

            # If no tool calls, we have the final answer
            if not msg.tool_calls:
                return msg.content or "(No response generated)"

            # Process each tool call
            messages.append(msg)  # Add assistant's tool-call message

            for tc in msg.tool_calls:
                tool_name = tc.function.name
                tool_args = json.loads(tc.function.arguments)

                print(f"   🔧 Using tool: {tool_name}({', '.join(f'{k}={v!r}' for k,v in tool_args.items())})")

                tool_result = self._execute_tool(tool_name, tool_args)

                # Add tool result to messages
                messages.append({
                    "role": "tool",
                    "tool_call_id": tc.id,
                    "content": tool_result,
                })

    # ─────────────────────────────────────────────
    # CAPABILITY 1: Enhanced Email Writer
    # ─────────────────────────────────────────────
    def write_email(
        self,
        description: str,
        tone: str = "formal",
        research: bool = False
    ) -> str:
        """
        Write a complete email based on a description.

        Args:
            description: What the email is about
            tone: 'formal', 'friendly', or 'casual'
            research: If True, searches the web before writing
        """
        # Input validation
        if not description.strip():
            return "❌ Please provide a description of the email you need."

        tone = tone.lower().strip()
        if tone not in ["formal", "friendly", "casual"]:
            print(f"   ⚠️  Unknown tone '{tone}', defaulting to 'formal'.")
            tone = "formal"

        system_prompt = f"""You are an expert email writer. Your task is to write a complete,
professional email based on the user's description.

Tone: {tone.upper()}
- formal: Professional, respectful, structured. Use proper salutations and closings.
- friendly: Warm and approachable but still professional.
- casual: Relaxed, conversational. OK to use contractions and informal language.

ALWAYS output in this exact format:
Subject: [subject line here]

[full email body]"""

        research_note = "Use the web_search tool to research relevant facts before writing if needed." if research else ""

        messages = [
            {"role": "system", "content": system_prompt},
            {"role": "user",   "content": f"Write a {tone} email about: {description}\n{research_note}"}
        ]

        print(f"\n📧 Email Writer | Tone: {tone} | Research: {'Yes' if research else 'No'}")
        print("   Processing...")

        try:
            result = self._run_agent_loop(messages)
            return result
        except Exception as e:
            return f"❌ Email generation failed: {str(e)}"

    # ─────────────────────────────────────────────
    # CAPABILITY 2: Smart Summarizer
    # ─────────────────────────────────────────────
    def summarize(
        self,
        text: str,
        length: str = "medium"
    ) -> str:
        """
        Summarize text and show analytics.

        Args:
            text: The text to summarize
            length: 'short' (1 sentence), 'medium' (2-3 sentences), 'long' (4-5 sentences)
        """
        # Input validation
        if not text.strip():
            return "❌ Please provide text to summarize."
        if len(text.split()) < 10:
            return "❌ Text is too short to summarize (minimum 10 words)."

        length = length.lower().strip()
        length_map = {
            "short":  "1 concise sentence",
            "medium": "2-3 sentences",
            "long":   "4-5 sentences covering main points"
        }
        if length not in length_map:
            length = "medium"
        length_instruction = length_map[length]

        # Calculate analytics BEFORE summarizing
        original_words = len(text.split())
        original_chars = len(text)
        original_sentences = text.count('.') + text.count('!') + text.count('?')

        print(f"\n📝 Summarizer | Length: {length} | Input: {original_words} words")
        print("   Processing...")

        messages = [
            {
                "role": "system",
                "content": f"You are a precise text summarizer. Summarize the given text in exactly {length_instruction}. Be accurate and capture the main idea. Return ONLY the summary, nothing else."
            },
            {"role": "user", "content": f"Summarize this text:\n\n{text}"}
        ]

        try:
            # Summarizer doesn't need tools — call directly
            response = self._call_llm(messages, use_tools=False)
            summary = response.choices[0].message.content.strip()

            # Post-summary analytics
            summary_words = len(summary.split())
            summary_chars = len(summary)
            reduction_pct = round((1 - summary_words / original_words) * 100, 1) if original_words else 0

            # Format output
            output = f"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📊 SUMMARY ANALYTICS
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  Original : {original_words:>5} words | {original_chars:>6} characters | ~{original_sentences} sentences
  Summary  : {summary_words:>5} words | {summary_chars:>6} characters
  Reduced  : {reduction_pct}% shorter
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📝 SUMMARY ({length.upper()})
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
{summary}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"""
            return output

        except Exception as e:
            return f"❌ Summarization failed: {str(e)}"

    # ─────────────────────────────────────────────
    # CAPABILITY 3: Chat Assistant (with Memory)
    # ─────────────────────────────────────────────
    def chat(self, user_message: str) -> str:
        """
        Chat with the assistant. Maintains conversation history.
        The assistant can use all 4 tools during chat.

        Args:
            user_message: The user's message
        """
        if not user_message.strip():
            return "❌ Please type a message!"

        SYSTEM_PROMPT = """You are a helpful, knowledgeable AI assistant. You have access to tools:
- calculate: for any math
- web_search: to look up information
- analyze_data: to process lists of numbers
- get_weather: to check weather in cities

Use tools when they'd genuinely help. Be concise, friendly, and accurate.
If asked about previous messages, refer to the conversation history."""

        # Add user message to history
        self.chat_history.append({"role": "user", "content": user_message})

        # Build full message list with system prompt
        messages = [
            {"role": "system", "content": SYSTEM_PROMPT}
        ] + self.chat_history

        try:
            response_text = self._run_agent_loop(messages)

            # Save assistant response to history
            self.chat_history.append({"role": "assistant", "content": response_text})

            return response_text

        except Exception as e:
            error_msg = f"❌ Chat error: {str(e)}"
            self.chat_history.append({"role": "assistant", "content": error_msg})
            return error_msg

    def clear_chat_history(self):
        """Clear conversation memory."""
        self.chat_history = []
        print("🗑️  Chat history cleared.")

    def show_chat_history(self):
        """Display the conversation history."""
        if not self.chat_history:
            print("📭 No chat history yet.")
            return
        print(f"\n💬 Chat History ({len(self.chat_history)} messages)")
        print("═" * 50)
        for i, msg in enumerate(self.chat_history, 1):
            role = "🧑 You" if msg["role"] == "user" else "🤖 Assistant"
            content = msg["content"][:200] + "..." if len(msg["content"]) > 200 else msg["content"]
            print(f"[{i}] {role}: {content}")
        print("═" * 50)

    # ─────────────────────────────────────────────
    # SMART ROUTER: Detect capability from input
    # ─────────────────────────────────────────────
    def process(self, user_input: str) -> str:
        """
        Auto-routes user input to the right capability.
        - Detects 'email', 'summarize', etc. and routes automatically.
        - Falls back to chat for everything else.
        """
        if not user_input.strip():
            return "❌ Please enter something!"

        text = user_input.lower()

        # Route to Email Writer
        if any(kw in text for kw in ["write email", "draft email", "compose email", "send email"]):
            tone = "formal"
            if "friendly" in text: tone = "friendly"
            if "casual" in text:   tone = "casual"
            research = "research" in text or "search" in text
            return self.write_email(user_input, tone=tone, research=research)

        # Route to Summarizer
        if any(kw in text for kw in ["summarize", "summarise", "summary of", "tldr", "tl;dr"]):
            # Try to extract the text after the keyword
            for kw in ["summarize:", "summarise:", "summary of:", "summarize ", "summarise "]:
                if kw in text:
                    content = user_input[user_input.lower().index(kw) + len(kw):].strip()
                    if content:
                        length = "short" if "short" in text else "long" if "long" in text else "medium"
                        return self.summarize(content, length=length)
            return "❌ Please provide text after 'summarize:'"

        # Everything else → Chat
        return self.chat(user_input)


