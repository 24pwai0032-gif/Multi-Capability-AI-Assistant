def analyze_data(data: list, operation: str = "all") -> str:
    """
    Analyzes a list of numbers.
    Operations: sum, average, max, min, median, std, all
    """
    try:
        # Validate input
        if not data:
            return json.dumps({"error": "Data list is empty.", "status": "error"})

        # Convert all to float
        nums = [float(x) for x in data]
        op = operation.lower().strip()

        ops_map = {
            "sum":     lambda n: sum(n),
            "average": lambda n: statistics.mean(n),
            "mean":    lambda n: statistics.mean(n),
            "max":     lambda n: max(n),
            "min":     lambda n: min(n),
            "median":  lambda n: statistics.median(n),
            "std":     lambda n: statistics.stdev(n) if len(n) > 1 else 0,
            "count":   lambda n: len(n),
        }

        if op == "all":
            result = {
                "count":   len(nums),
                "sum":     round(sum(nums), 4),
                "average": round(statistics.mean(nums), 4),
                "median":  round(statistics.median(nums), 4),
                "max":     max(nums),
                "min":     min(nums),
                "std_dev": round(statistics.stdev(nums), 4) if len(nums) > 1 else 0,
            }
        elif op in ops_map:
            result = {operation: round(ops_map[op](nums), 4)}
        else:
            return json.dumps({"error": f"Unknown operation '{operation}'. Use: sum, average, max, min, median, std, all", "status": "error"})

        return json.dumps({"data": nums, "result": result, "status": "success"})

    except (ValueError, TypeError) as e:
        return json.dumps({"error": f"Invalid data: {str(e)}", "status": "error"})


