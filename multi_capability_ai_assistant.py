def calculate(expression: str) -> str:
    """
    Safely evaluates a mathematical expression.
    Supports: +, -, *, /, **, sqrt, log, sin, cos, tan, pi, e
    """
    try:
        # Safe math context — only allow math functions
        allowed = {
            "__builtins__": {},
            "sqrt": math.sqrt,
            "log": math.log,
            "sin": math.sin,
            "cos": math.cos,
            "tan": math.tan,
            "pi": math.pi,
            "e": math.e,
            "abs": abs,
            "round": round,
        }
        result = eval(expression, allowed)
        return json.dumps({
            "expression": expression,
            "result": round(result, 6),
            "status": "success"
        })
    except ZeroDivisionError:
        return json.dumps({"error": "Division by zero is not allowed.", "status": "error"})
    except Exception as e:
        return json.dumps({"error": f"Could not evaluate: {str(e)}", "status": "error"})


