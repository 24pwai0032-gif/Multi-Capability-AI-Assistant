while True:
    try:
        user_input = input("\n🧑 You: ").strip()
    except (EOFError, KeyboardInterrupt):
        print("\n👋 Goodbye!")
        break

    if not user_input:
        print("   ⚠️  Please type something!")
        continue

    cmd = user_input.lower()

    if cmd in ["exit", "quit", "bye"]:
        print("👋 Goodbye! Hope I was helpful.")
        break
    elif cmd == "history":
        assistant.show_chat_history()
        continue
    elif cmd == "clear":
        assistant.clear_chat_history()
        continue
    elif cmd == "help":
        print(HELP_TEXT)
        continue

    # Process the input
    print("🤖 Assistant: ", end="", flush=True)
    response = assistant.process(user_input)
    print(response)

